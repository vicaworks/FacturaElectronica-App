package com.servitec.common.dao.exception;

/**
 * @author villaleda
 *
 */
public class NoExisteRegistroException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 894581777096257922L;

	/**
	 * 
	 */
	public NoExisteRegistroException() {
	}

	/**
	 * @param arg0
	 */
	public NoExisteRegistroException(String arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 */
	public NoExisteRegistroException(Throwable arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public NoExisteRegistroException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}

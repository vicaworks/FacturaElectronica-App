package com.servitec.common.util.exceptions;

/**
 * @author cvillarreal
 *
 */
public class ParametroRequeridoException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1853008867535372943L;

	/**
	 * 
	 */
	public ParametroRequeridoException() {
	}

	/**
	 * @param message
	 */
	public ParametroRequeridoException(String message) {
		super(message);
	}

	/**
	 * @param cause
	 */
	public ParametroRequeridoException(Throwable cause) {
		super(cause);
	}

	/**
	 * @param message
	 * @param cause
	 */
	public ParametroRequeridoException(String message, Throwable cause) {
		super(message, cause);
	}

}
